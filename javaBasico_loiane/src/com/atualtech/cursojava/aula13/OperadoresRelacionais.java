package com.atualtech.cursojava.aula13;

public class OperadoresRelacionais {
	public static void main(String[] args) {
		// Operadores Relacionais
		int valor1 = 1;
		int valor2 = 2;
		
		System.out.println("Valor1 == Valor2 = " + (valor1 == valor2));
		System.out.println("Valor1 != Valor2 = " + (valor1 != valor2));
		System.out.println("Valor1 > Valor2 = " + (valor1 > valor2));
		System.out.println("Valor1 < Valor2 = " + (valor1 < valor2));
		System.out.println("Valor1 >= Valor2 = " + (valor1 >= valor2));
		System.out.println("Valor1 >= Valor2 = " + (valor1 <= valor2));
	}
}
